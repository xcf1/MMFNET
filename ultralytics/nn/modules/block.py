class DetailBlockGhost(nn.Module):
    def __init__(self, in_channels, out_channels):
        super().__init__()
        self.ghost = GhostConv(in_channels, out_channels)
        self.bn = nn.BatchNorm2d(out_channels)
        self.act = nn.SiLU()

        self.edge_enhance = nn.Conv2d(out_channels, out_channels, 1, groups=out_channels)

    def forward(self, x):
        x = self.act(self.bn(self.ghost(x)))
        edge = self.edge_enhance(x)
        return x + edge


